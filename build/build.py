# ── Tracker DB from YAML ─────────────────────────────────────────────────────
def _js_esc(s: str) -> str:
    return s.replace("\\", "\\\\").replace("'", "\\'")

def _build_trackers_js() -> str:
    """Read trackers.yaml and emit the SR_FRAMING + SR_ACTION + TR JS block."""
    with open(BUILD_DIR / "trackers.yaml", encoding="utf-8") as _f:
        _td = yaml.safe_load(_f)
    _srf = _td["shared"]["sr_framing"]
    _sra = _td["shared"]["sr_action"]

    def _expand(text: str) -> str:
        return text.replace("<<SR_FRAMING>>", _srf).replace("<<SR_ACTION>>", _sra)

    lines = [
        "// ── TRACKER DB ────────────────────────────────────────────────",
        f"const SR_FRAMING = '{_js_esc(_srf)}';",
        f"const SR_ACTION = '{_js_esc(_sra)}';",
        "",
        "const TR = {",
    ]
    for t in _td["trackers"]:
        regs = ",".join(f"'{r}'" for r in t["regs"])
        d    = _js_esc(_expand(t["description"]))
        a    = _js_esc(_expand(t["action"]))
        pat  = _js_esc(t["pattern"])
        lines.append(
            f"  '{pat}':{{n:'{_js_esc(t['name'])}',cat:'{_js_esc(t['category'])}',"
            f"cc:'{t['cc']}',r:'{t['risk']}',regs:[{regs}],"
            f"d:'{d}',a:'{a}'}},"
        )
    lines.append("};")
    return "\n".join(lines)

def _build_citations_js() -> str:
    """Read citations.json and emit LEGAL_REFERENCES_INDEX + LEGAL_REG_MAP JS blocks."""
    with open(BUILD_DIR / "citations.json", encoding="utf-8") as _f:
        _cd = json.load(_f)

    def _js_str(s: str) -> str:
        return s.replace("\\", "\\\\").replace("'", "\\'")

    ref_lines = ["const LEGAL_REFERENCES_INDEX = {"]
    for key, entry in _cd["references"].items():
        ref_lines.append(
            f"  {key}: {{"
            f"short: '{_js_str(entry['short'])}',"
            f"citation: '{_js_str(entry['citation'])}'}},"
        )
    ref_lines.append("};")

    map_lines = ["const LEGAL_REG_MAP = {"]
    for reg, keys in _cd["reg_map"].items():
        keys_str = ",".join(f"'{k}'" for k in keys)
        map_lines.append(f"  '{_js_str(reg)}':[{keys_str}],")
    map_lines.append("};")

    return "\n".join(ref_lines) + "\n\n" + "\n".join(map_lines)

